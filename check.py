
import math
def sum(a,b):
    return a + b

def difference(a,b):
    return a - b

def product(a,b):
    return a * b

def quotient(a,b):
    return a / b

def power(a,b):
    return a ** b

def remainder(a,b):
    return a % b

def logarithm(a,b):
    return math.log(a,b)

def sine(a):
    return math.sin(a)

def cosine(a):
    return math.cos(a)

def tangent(a):
    return math.tan(a)
